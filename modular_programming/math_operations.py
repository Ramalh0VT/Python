def addition(num1, num2):
    final_result = num1 + num2
    return final_result

def subtraction(num1, num2): 
    final_result = num1 - num2
    return final_result

def multiplication(num1, num2):
    final_result = num1 * num2
    return final_result

def division (num1, num2):
    final_result = num1 / num2
    return final_result

